const mongoose = require("mongoose");

var userSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    }
});

var UserModel = mongoose.model('UserModel', userSchema);
module.exports = UserModel;